function missingVals(vclay,vsilt,vsand){

	var weight_ary = [];
	var dul = 0;
	var ll = 0;
	var result = [];
	var json_raw = jsonData();
	var obj = JSON.parse(json_raw);
	var target = {clay:vclay,silt:vsilt,sand:vsand}; 

	obj.rows.forEach(function(row){
		row.distance = distance(row).toFixed(2);
	});

	obj.rows.sort(function(a, b){
		var a_new = parseFloat(a.distance);
		var b_new = parseFloat(b.distance);
	    if(a_new > b_new)
	        return 1;
	    if(a_new < b_new)
	        return -1;
	    return 0;
	});

	/*obj.rows.forEach(function(row){
		console.log(row);
	});*/
	weights();
	calcDUL();
	calcLL();
	dul = dul * 100;
	ll = ll * 100;
	result = {dul:dul.toFixed(1),ll:ll.toFixed(1)}; 
	/*console.log("dul =" +  dul);
	console.log("ll = " +  ll);*/

	return result;

	function distance(row){
		var w_clay = 1.82;
		var w_silt = 1.00;
		var w_sand = 1.13;

		var diff_clay = Math.pow(target.clay-row.CLAY, 2);
		var diff_sand = Math.pow(target.sand-row.SAND, 2);
		var diff_silt = Math.pow(target.silt-row.Silt, 2);

		return (w_clay*(diff_clay)) + (w_sand*(diff_sand)) + (w_silt*(diff_silt)); 
	}

	function weights(){
		var weight_denom = 1/1+1/2+1/3+1/4+1/5+1/6;
		for(i=1;i<7;i++){
			var weight = (1/i)/weight_denom;
			weight_ary.push(weight);
		}
	}

	function calcDUL(){
		for(i=0;i<weight_ary.length;i++){
			dul = dul + weight_ary[i]*obj.rows[i].DUL;
		}
	}

	function calcLL(){
		for(i=0;i<weight_ary.length;i++){
			//console.log("ll"+i+" clay "+obj.rows[i].CLAY+" sand "+obj.rows[i].SAND+" silt "+obj.rows[i].Silt);
			ll = ll + weight_ary[i]*obj.rows[i].LL;
		}
	}
}
